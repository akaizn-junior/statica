#!/usr/bin/env node
/**
 * Generate a multi-platform Homebrew formula from GitHub release assets.
 *
 * Usage:
 *   node scripts/update-homebrew-formula.mjs \
 *     --version 0.12.0 \
 *     --repo owner/statica \
 *     --assets-dir ./release-assets \
 *     --output ./homebrew/statica.rb
 */

import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const TARGETS = [
  { triple: "aarch64-apple-darwin", platform: "macos", arch: "arm", installName: "statica-aarch64-apple-darwin" },
  { triple: "x86_64-apple-darwin", platform: "macos", arch: "intel", installName: "statica-x86_64-apple-darwin" },
  { triple: "aarch64-unknown-linux-gnu", platform: "linux", arch: "arm", installName: "statica-aarch64-unknown-linux-gnu" },
  { triple: "x86_64-unknown-linux-gnu", platform: "linux", arch: "intel", installName: "statica-x86_64-unknown-linux-gnu" },
];

function parseArgs(argv) {
  const out = { version: null, repo: null, assetsDir: null, output: null };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--version") out.version = argv[++i];
    else if (arg === "--repo") out.repo = argv[++i];
    else if (arg === "--assets-dir") out.assetsDir = path.resolve(argv[++i]);
    else if (arg === "--output") out.output = path.resolve(argv[++i]);
    else if (arg === "--help" || arg === "-h") {
      console.log(`Usage: node scripts/update-homebrew-formula.mjs \\
  --version X.Y.Z --repo owner/statica --assets-dir DIR [--output FILE]`);
      process.exit(0);
    } else {
      console.error(`Unknown argument: ${arg}`);
      process.exit(1);
    }
  }
  if (!out.version || !out.repo || !out.assetsDir) {
    console.error("Required: --version, --repo, --assets-dir");
    process.exit(1);
  }
  return out;
}

function sha256File(filePath) {
  const hash = crypto.createHash("sha256");
  hash.update(fs.readFileSync(filePath));
  return hash.digest("hex");
}

function findAsset(assetsDir, triple) {
  const flat = path.join(assetsDir, `statica-${triple}`);
  if (fs.existsSync(flat) && fs.statSync(flat).isFile()) return flat;
  const releaseNested = path.join(assetsDir, `statica-${triple}`, "statica");
  if (fs.existsSync(releaseNested)) return releaseNested;
  const ciNested = path.join(assetsDir, triple, "statica");
  if (fs.existsSync(ciNested)) return ciNested;
  throw new Error(`missing release asset for ${triple} under ${assetsDir}`);
}

function renderPlatformBlock({ platform, arch, url, sha256 }) {
  const archLine = arch === "arm" ? "    on_arm do" : "    on_intel do";
  return [
    archLine,
    `      url "${url}"`,
    `      sha256 "${sha256}"`,
    "    end",
  ].join("\n");
}

function generateFormula({ version, repo, assetsDir }) {
  const tag = `v${version}`;
  const homepage = `https://github.com/${repo}`;
  const entries = TARGETS.map((target) => {
    const asset = findAsset(assetsDir, target.triple);
    return {
      ...target,
      url: `${homepage}/releases/download/${tag}/statica-${target.triple}`,
      sha256: sha256File(asset),
    };
  });

  const macBlocks = entries
    .filter((entry) => entry.platform === "macos")
    .map(renderPlatformBlock)
    .join("\n");
  const linuxBlocks = entries
    .filter((entry) => entry.platform === "linux")
    .map(renderPlatformBlock)
    .join("\n");

  const installLines = entries.map((entry) => {
    const condition =
      entry.platform === "macos"
        ? entry.arch === "arm"
          ? "OS.mac? && Hardware::CPU.arm?"
          : "OS.mac? && Hardware::CPU.intel?"
        : entry.arch === "arm"
          ? "OS.linux? && Hardware::CPU.arm?"
          : "OS.linux? && Hardware::CPU.intel?";
    return `    bin.install "${entry.installName}" => "statica" if ${condition}`;
  });

  return `# typed: false
# frozen_string_literal: true

# Generated by scripts/update-homebrew-formula.mjs — do not edit by hand.
class Statica < Formula
  desc "Powered HTML static site generator"
  homepage "${homepage}"
  license "MIT"
  version "${version}"

  on_macos do
${macBlocks}
  end

  on_linux do
${linuxBlocks}
  end

  def install
${installLines.join("\n")}
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/statica -v")
  end
end
`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const formula = generateFormula(args);
  if (args.output) {
    fs.mkdirSync(path.dirname(args.output), { recursive: true });
    fs.writeFileSync(args.output, formula);
    console.log(`Wrote ${args.output}`);
  } else {
    process.stdout.write(formula);
  }
}

main();
